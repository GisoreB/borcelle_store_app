class User{
  late int id;
  late String email;
  late String username;
  late String password;
  late String name;
  late String firstname;
  late String lastname;
  late String address;
  late String city;
  late String street;
  late int number;
  late String zipcode;
  late String phone;

  User({required this.id, required this.email, required this.username,
    required this.password, required this.name, required this.firstname,
    required this.lastname, required this.address, required this.city,
    required this.street, required this.number, required this.zipcode,
    required this.phone});
}